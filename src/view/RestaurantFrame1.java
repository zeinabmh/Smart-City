    private void rcbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rcbActionPerformed
        platespanel.setVisible(true);
        orderpanel.setVisible(false);
        
        String restoname = rcb.getSelectedItem().toString();
        String allplates [] = rst.getRestoMenu(restoname);
        
        for(int i=0 ; i<allplates.length ; i++)
        //System.out.println(allplates[i]);
        
        mcb.setModel(new DefaultComboBoxModel(allplates));
        
        String text = inv.getText();
        if(!text.isEmpty())
            JOptionPane.showMessageDialog(this , "You can't order from 2 restaurants at same time!" + "\n" + "Clear cart to order from another restaurant");
        
    }//GEN-LAST:event_rcbActionPerformed

