    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String platename = mcb.getSelectedItem().toString();
       String unitPrice = price.getText();
       String qty = nb.getValue().toString();
        
        rst.addToCart(platename , unitPrice,Integer.parseInt(qty));
        inv.setText(rst.printCart());
        counter.setText(rst.getPurchasedQuantity()+"");
        tot.setText(rst.calculateTotaltoPay()+"");
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String selection = inv.getSelectedText();
        String [] s = selection.split("\t");
        
        rst.removePurchase(s[0]);
        inv.setText(rst.printCart());
        counter.setText(rst.getPurchasedQuantity() + "");
        tot.setText(rst.calculateTotaltoPay()+"");
    }//GEN-LAST:event_jButton1ActionPerformed

