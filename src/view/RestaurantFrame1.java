    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String total = tot.getText();
        
        int result = JOptionPane.showConfirmDialog(null , "Your total amount is " + total + "L.L , Are you sure you want to confirm and pay? " , "Confirmation" , JOptionPane.YES_NO_OPTION);
        
        if(result == JOptionPane.YES_OPTION){
           app.addPurchase(rst.getCart());
           nb.setValue(0);
           inv.setText("");
           counter.setText("0");
           tot.setText("0");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

